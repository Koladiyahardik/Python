'''>>>>>>>> DATA TYPE <<<<<<<<<<< '''

'''-------Built-in DataType------'''

#******* Numeric **********

'''Integer'''
a=1000
print(a,type(a))

'''Float'''
b=10.1
print(b,type(b))

'''Complex'''
c=5+7j
print(c,type(c))

'''Bool'''
d=True
print(d,type(d))

#******* Sequences **********

'''STRING'''
e="Hardik"
print(e,type(e))

'''LIST'''
f=[10,'hardik',10.1,1+7j,True]
print(f,type(f))

'''Tuple'''
g=(10,'Hardik',10.1,1+7j,True)
print(g,type(g))

'''RANGE'''
h=range(5)
print(h,type(h))

'''SET'''
i={1,'Hardik',10.1,1+7j,True}
print(i,type(i))

'''DICTIONARY'''
j={'Enroll':[101,102,103],
   'Name':['Hardik','Jeet','Udit']}
print(j,type(j))


