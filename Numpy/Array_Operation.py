import numpy as np

array_1=np.array([1,2,3,4,5])
array_2=np.array([6,7,8,9,10])

# Using the + Operator
result1=array_1+array_2
print("Using the + Operator:",result1)

#  Using the add() function
result2=np.add(array_1,array_2)
print("Using add() Function:",result2,"\n")

# Using the - Operator
result1=array_1-array_2
print("Using the - Operator:",result1)

#  Using the subtract() function
result2=np.subtract(array_1,array_2)
print("Using subtract() Function:",result2,"\n")

# Using the * Operator
result1=array_1*array_2
print("Using the * Operator:",result1)

#  Using the multiplication() function
result2=np.multiply(array_1,array_2)
print("Using multi() Function:",result2,"\n")

# Using the / Operator
result1=array_1/array_2
print("Using the / Operator:",result1)

#  Using the divide() function
result2=np.divide(array_1,array_2)
print("Using divide() Function:",result2,"\n")

